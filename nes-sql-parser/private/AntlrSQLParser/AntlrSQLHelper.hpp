#pragma once

#include <cstddef>
#include <cstdint>
#include <memory>
#include <optional>
#include <string>
#include <vector>
#include <Functions/FieldAccessLogicalFunction.hpp>
#include <Functions/FieldAssignmentLogicalFunction.hpp>
#include <Operators/Windows/Aggregations/WindowAggregationLogicalFunction.hpp>
#include <Operators/Windows/JoinLogicalOperator.hpp>
#include <Plans/QueryPlan.hpp>
#include <Sinks/SinkDescriptor.hpp>

namespace NES::Parsers
{

class AntlrSQLHelper
{
    std::vector<LogicalFunction> projectionFields; ///vector needed for logicalProjectionOperator constructor
    std::vector<LogicalFunction> whereClauses; ///where and having clauses need to be accessed in reverse
    std::vector<LogicalFunction> havingClauses;
    std::string source;

public:
    /// Constructors
    AntlrSQLHelper() = default;


    /// Copy constructor -- performs deep copy by cloning each pointed object.
    AntlrSQLHelper(const AntlrSQLHelper & other)
        : source(other.source)
        , isSelect(other.isSelect)
        , isWhereOrHaving(other.isWhereOrHaving)
        , isFrom(other.isFrom)
        , isWindow(other.isWindow)
        , isArithmeticBinary(other.isArithmeticBinary)
        , isJoinRelation(other.isJoinRelation)
        , isFunctionCall(other.isFunctionCall)
        , isSetOperation(other.isSetOperation)
        , isGroupBy(other.isGroupBy)
        , joinSources(other.joinSources)
        , joinSourceRenames(other.joinSourceRenames)
        , joinType(other.joinType)
        , opBoolean(other.opBoolean)
        , opValue(other.opValue)
        , newSourceName(other.newSourceName)
        , timestamp(other.timestamp)
        , size(other.size)
        , advanceBy(other.advanceBy)
        , timeUnit(other.timeUnit)
        , timeUnitAdvanceBy(other.timeUnitAdvanceBy)
        , minimumCount(other.minimumCount)
        , identCountHelper(other.identCountHelper)
        , implicitMapCountHelper(other.implicitMapCountHelper)
    {
        // Deep copy each unique_ptr vector via clone() (assuming clone() is defined for each type)

        for (const auto & pf : other.projectionFields)
        {
            projectionFields.push_back(pf ? pf->clone() : nullptr);
        }

        for (const auto & wc : other.whereClauses)
        {
            whereClauses.push_back(wc ? wc->clone() : nullptr);
        }

        for (const auto & hc : other.havingClauses)
        {
            havingClauses.push_back(hc ? hc->clone() : nullptr);
        }

        for (const auto & qp : other.queryPlans)
        {
            queryPlans.push_back(qp);
        }

        if (other.windowType)
        {
            windowType = other.windowType->clone();
        }

        for (const auto & wa : other.windowAggs)
        {
            windowAggs.push_back(wa ? wa->clone() : nullptr);
        }

        for (const auto & proj : other.projections)
        {
            projections.push_back(proj ? proj->clone() : nullptr);
        }

        for (const auto & sd : other.sinkDescriptor)
        {
            sinkDescriptor.push_back(sd ? sd->clone() : nullptr);
        }

        for (const auto & fb : other.functionBuilder)
        {
            functionBuilder.push_back(fb ? fb->clone() : nullptr);
        }

        /*
        for (const auto & mb : other.mapBuilder)
        {
            mapBuilder.push_back(mb ? mb->clone() : nullptr);
        }

        for (const auto & gb : other.groupByFields)
        {
            groupByFields.push_back(gb ? gb->clone() : nullptr);
        }*/

        if (other.joinFunction)
        {
            joinFunction = other.joinFunction->clone();
        }

        for (const auto & jk : other.joinKeyRelationHelper)
        {
            joinKeyRelationHelper.push_back(jk ? jk->clone() : nullptr);
        }
    }

    /// Query plans generated by antlr sql query parser.
    std::vector<QueryPlan> queryPlans;

    /// Flags used to determine which token we are currently parsing.
    bool isSelect = false;
    bool isWhereOrHaving = false;
    bool isFrom = false;
    bool isWindow = false;
    bool isArithmeticBinary = false;
    bool isJoinRelation = false;
    bool isFunctionCall = false;
    bool isSetOperation = false;
    bool isGroupBy = false;

    /// Containers that hold state of specific objects that we create during parsing.
    std::unique_ptr<Windowing::WindowType> windowType;
    std::vector<std::unique_ptr<WindowAggregationLogicalFunction>> windowAggs;
    std::vector<LogicalFunction> projections;
    std::vector<std::unique_ptr<Sinks::SinkDescriptor>> sinkDescriptor;
    std::vector<LogicalFunction> functionBuilder;
    std::vector<std::unique_ptr<FieldAssignmentLogicalFunction>> mapBuilder;
    std::vector<std::unique_ptr<FieldAccessLogicalFunction>> groupByFields;
    std::vector<std::string> joinSources;
    LogicalFunction joinFunction;
    std::vector<LogicalFunction> joinKeyRelationHelper;
    std::vector<std::string> joinSourceRenames;
    Join::LogicalJoinDescriptor::JoinType joinType;

    /// Utility variables to keep state between enter/exit parser function calls.
    size_t opBoolean; ///anonymous token enum in AntlrSQLLexer.h
    std::string opValue;
    std::string newSourceName;
    std::string timestamp;

    /// Utility variables used to keep track of the parsing state.
    int size;
    int advanceBy;
    size_t timeUnit; ///anonymous token enum in AntlrSQLLexer.h
    size_t timeUnitAdvanceBy;
    std::optional<int> minimumCount;
    int identCountHelper = 0;
    int implicitMapCountHelper = 0;

    [[nodiscard]] std::vector<LogicalFunction>& getWhereClauses();
    [[nodiscard]] std::vector<LogicalFunction>& getHavingClauses();
    [[nodiscard]] std::vector<LogicalFunction>& getProjectionFields();
    [[nodiscard]] std::vector<std::unique_ptr<FieldAssignmentLogicalFunction>>& getMapExpressions();

    void addWhereClause(LogicalFunction expressionNode);
    void addHavingClause(LogicalFunction expressionNode);
    void addProjectionField(LogicalFunction expressionNode);
    [[nodiscard]] static std::unique_ptr<Windowing::WindowType> getWindowType();
    void setSource(std::string sourceName);
    const std::string getSource() const;
    void addMapExpression(std::unique_ptr<FieldAssignmentLogicalFunction> expressionNode);
    void setMapExpressions(std::vector<std::unique_ptr<FieldAssignmentLogicalFunction>> expressions);
};
}