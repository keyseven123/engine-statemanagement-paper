#pragma once

#include <cstddef>
#include <cstdint>
#include <optional>
#include <string>
#include <vector>
#include <LogicalFunctions/FieldAccessFunction.hpp>
#include <LogicalFunctions/FieldAssignmentFunction.hpp>
#include <LogicalOperators/Windows/Aggregations/WindowAggregationFunction.hpp>
#include <LogicalOperators/Windows/JoinOperator.hpp>
#include <LogicalPlans/Plan.hpp>
#include <Sinks/SinkDescriptor.hpp>

using namespace Logical;

namespace NES::Parsers
{

class AntlrSQLHelper
{
    std::vector<Function> projectionFields; ///vector needed for logicalProjectionOperator constructor
    std::vector<Function> whereClauses; ///where and having clauses need to be accessed in reverse
    std::vector<Function> havingClauses;
    std::string source;

public:
    /// Constructors
    AntlrSQLHelper() = default;

    /// Query plans generated by antlr sql query parser.
    std::vector<Plan> queryPlans;

    /// Flags used to determine which token we are currently parsing.
    bool isSelect = false;
    bool isWhereOrHaving = false;
    bool isFrom = false;
    bool isWindow = false;
    bool isArithmeticBinary = false;
    bool isJoinRelation = false;
    bool isSimpleCondition = true;
    bool isTimeBasedWindow = true;
    bool isSetOperation = false;
    bool isGroupBy = false;
    bool hasMultipleAttributes = false;
    bool isInFunctionCall() const { return not functionBuilder.empty(); }
    bool isInAggFunction() const { return not windowAggs.empty(); }

    /// Containers that hold state of specific objects that we create during parsing.
    std::shared_ptr<Windowing::WindowType> windowType;
    std::vector<std::shared_ptr<WindowAggregationFunction>> windowAggs;
    std::vector<Function> projections;
    std::vector<Sinks::SinkDescriptor> sinkDescriptor;
    std::vector<std::string> constantBuilder;
    std::vector<Function> functionBuilder;
    std::vector<FieldAssignmentFunction> mapBuilder;
    std::vector<FieldAccessFunction> groupByFields;
    std::vector<std::string> joinSources;
    std::optional<Function> joinFunction;
    std::vector<Function> joinKeyRelationHelper;
    std::vector<std::string> joinSourceRenames;
    JoinOperator::JoinType joinType;

    /// Utility variables to keep state between enter/exit parser function calls.
    size_t opBoolean; ///anonymous token enum in AntlrSQLLexer.h
    std::string opValue;
    std::string newSourceName;
    std::string timestamp;

    /// Utility variables used to keep track of the parsing state.
    int size;
    int advanceBy;
    size_t timeUnit; ///anonymous token enum in AntlrSQLLexer.h
    size_t timeUnitAdvanceBy;
    std::optional<int> minimumCount;
    int implicitMapCountHelper = 0;

    [[nodiscard]] std::vector<Function>& getWhereClauses();
    [[nodiscard]] std::vector<Function>& getHavingClauses();
    [[nodiscard]] std::vector<Function>& getProjectionFields();
    [[nodiscard]] std::vector<FieldAssignmentFunction>& getMapExpressions();

    void addWhereClause(Function expressionNode);
    void addHavingClause(Function expressionNode);
    void addProjectionField(FieldAccessFunction expressionNode);
    [[nodiscard]] static std::shared_ptr<Windowing::WindowType> getWindowType();
    void setSource(std::string sourceName);
    const std::string getSource() const;
    void addMapExpression(FieldAssignmentFunction expressionNode);
    void setMapExpressions(std::vector<FieldAssignmentFunction> expressions);
};
}
