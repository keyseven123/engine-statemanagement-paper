#ifndef NES_INCLUDE_COMPILER_SOURCECODE_HPP_
#define NES_INCLUDE_COMPILER_SOURCECODE_HPP_
#include <string>
namespace NES::Compiler {

/**
 * @brief Represents a source code artifact.
 * Usually generated by a code generator.
 */
class SourceCode {
  public:
    /**
     * @brief Constructor to create a new source code artifact.
     * @param language the language of this source code artifact.
     * @param code the code
     */
    SourceCode(std::string language, std::string code);

    /**
     * @brief Returns the language of this source code.
     * @return std::string
     */
    [[nodiscard]] const std::string& getLanguage() const;

    /**
     * @brief Returns the code.
     * @return std::string
     */
    [[nodiscard]] const std::string& getCode() const;

  private:
    const std::string code;
    const std::string language;
};

}// namespace NES::Compiler

#endif  // NES_INCLUDE_COMPILER_SOURCECODE_HPP_
